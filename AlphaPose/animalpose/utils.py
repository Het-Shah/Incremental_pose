import torch
import numpy as np

def L2_dist(v1, v2):
    v1 = np.array(v1)
    v2 = np.array(v2)
    return np.sqrt(np.abs(np.sum((v2 - v1)**2)))

def calc_dist(avg_vector, animal_list):
    result = []
    for i in animal_list:
        fname = i[0]
        keypoints = i[1]
        dist = L2_dist(avg_vector, keypoints)
        temp = ((fname, keypoints), dist)
        result.append(temp)
    return result

